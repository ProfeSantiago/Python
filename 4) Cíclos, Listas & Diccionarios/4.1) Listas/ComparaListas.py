#---------------------------------------------------------
# Ejemplo básico de comparación de Listas
#---------------------------------------------------------

primeraLista = [5, 2, 0, 1, 3, 4]

segundaLista = [1, 2, 3, 5, 4, 0]

terceraLista = [1, 2, 3, 5, 4, 0]

print("\nLas listas son iguales? " + str(primeraLista == segundaLista))

print("\nLas listas son iguales? " + str(terceraLista == segundaLista))

#---------------------------------------------------------
#Nota:
#   Como las Listas son contenedores ordenados
#   deben ser iguales tanto en el valor y
#   cantidad de elementos y en orden de los mismos.
