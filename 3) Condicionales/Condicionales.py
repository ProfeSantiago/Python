if 34 == 34:
    print("34 es igual que 34")

if 99 != 34:
    print("99 es NO igual que 34")

if 21 > 20:
    print("21 es mayor que 20")

if 7 < 60:
    print("7 es menor que 60")

if 44.5 >= 44:
    print("45.5 es mayor o igual que 45")

if 3 <= 98:
    print("3 es menor o igual que 98")


