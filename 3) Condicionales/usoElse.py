#--------------------------------------------
# Uso del else
#--------------------------------------------

if 43 >= 73:
    print("El primer número es mayor o igual al segundo")
else:
    print("El primer número es menor que segundo")

#--------------------------------------------
# Uso del elif
#--------------------------------------------

if 21 > 21:
    print("El primer número es mayor al segundo")
elif 21 == 21:
    print("El primer número es igual al segundo")
elif 21 < 21:
    print("El primer número es menor al segundo")



